void motor_enable(void) {
	//PHASE1_on();
	GPIO_SetBits(GPIOA,GPIO_Pin_7) ;
	//PHASE2_on();
	GPIO_SetBits(GPIOE,GPIO_Pin_12) ;
	//nSLEEP1_on();
	GPIO_SetBits(GPIOA,GPIO_Pin_6) ;
	//nSLEEP2_on();
	GPIO_SetBits(GPIOB,GPIO_Pin_1) ;
}

